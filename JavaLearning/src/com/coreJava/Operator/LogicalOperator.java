package com.coreJava.Operator;

public class LogicalOperator {
    public static void main(String[] args) {
        boolean a=false;
        boolean b=true;
        System.out.println(a&&b);
        System.out.println(a||b);
        System.out.println(!a);
        System.out.println(!b);
        System.out.println(!a&&b);
        System.out.println(a==b); // comparison operator
    }
}
